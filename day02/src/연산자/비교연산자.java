package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {
		/*
			비교연산자 : ==, !=, >, >=, <, <=
			비교결과 중요( 논리데이터, boolean, true/false )
		*/	
			
		int x = 100;
		int y = 200;
		
		System.out.println( x == y);
		System.out.println( x != y);
		System.out.println( x >= y);
		System.out.println( x <= y);
		System.out.println( x > y);
		System.out.println( x < y);
		
		
	}

}
