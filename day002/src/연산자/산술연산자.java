package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		//	산술연산자 : 사칙연산자 ( +, -, *아스테르크, /, % )
		int x = 100;
		int y = 200;
		
		System.out.println("합 " + (x + y));
		System.out.println("빼기 " + (x - y));
		System.out.println("곱하기 " + (x * y));
		System.out.println("나누기 " + (x / y));
		System.out.println("나머지 " + (x % y));
		
	}

}
